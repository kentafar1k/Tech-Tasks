def calculate_sum():
    total = 0
    with open("numbers.txt", "r") as file:
        for line in file:
            try:
                # Удаляем пробелы и преобразуем в число
                num = line.strip()
                if num:  # Проверяем, что строка не пустая
                    total += float(num)
            except ValueError:
                print(f"Ошибка при преобразовании числа: {line.strip()}")

    print(f"Сумма чисел: {total}")

